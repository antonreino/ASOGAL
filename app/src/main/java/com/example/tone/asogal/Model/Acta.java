public class Acta {
    private String IdJugador,IdActa,Palos, TarjetaAmarilla, TarjetaRoja, TarjetaNegra,Lesion,Goles;

    public Acta() {
    }

    public String getIdJugador() {
        return IdJugador;
    }

    public void setIdJugador(String idJugador) {
        IdJugador = idJugador;
    }

    public String getIdActa() {
        return IdActa;
    }

    public void setIdActa(String idActa) {
        IdActa = idActa;
    }

    public String getPalos() {
        return Palos;
    }

    public void setPalos(String palos) {
        Palos = palos;
    }

    public String getTarjetaAmarilla() {
        return TarjetaAmarilla;
    }

    public void setTarjetaAmarilla(String tarjetaAmarilla) {
        TarjetaAmarilla = tarjetaAmarilla;
    }

    public String getTarjetaRoja() {
        return TarjetaRoja;
    }

    public void setTarjetaRoja(String tarjetaRoja) {
        TarjetaRoja = tarjetaRoja;
    }

    public String getTarjetaNegra() {
        return TarjetaNegra;
    }

    public void setTarjetaNegra(String tarjetaNegra) {
        TarjetaNegra = tarjetaNegra;
    }

    public String getLesion() {
        return Lesion;
    }

    public void setLesion(String lesion) {
        Lesion = lesion;
    }

    public String getGoles() {
        return Goles;
    }

    public void setGoles(String goles) {
        Goles = goles;
    }
}
